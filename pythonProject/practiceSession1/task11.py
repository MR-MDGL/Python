 # Create a program that demonstrates the usage of bitwise operators.
 #    1. Define two integers, a and b, with values of your choice.
 #    2. Perform the following bitwise operations and print the results:
 #        ◦ AND (&)
 #        ◦ OR (|)
 #        ◦ XOR (^)
 #        ◦ NOT (~)
 #        ◦ Left Shift (<<)
 #        ◦ Right Shift (>>)
a=1
b=0
print(a & b)
print(a | b)
print(a ^ b )
print(~ a )
print( a << 1)
print( a >> 1)
print( b << 1)
print( b >> 1)
